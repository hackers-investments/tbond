require('./utils.js').imports()

task('rework').setAction(async () => {
  start_impersonate(MGR)
  start_impersonate(WTON)
  const [admin, user1, user2] = await ethers.getSigners()
  await network.provider.send('hardhat_setBalance', [MGR, '0x100000000000000000'])
  await network.provider.send('hardhat_setBalance', [WTON, '0x100000000000000000'])
  const ton = new ethers.Contract(TON, ABI, await ethers.getSigner(WTON))
  const wton = new ethers.Contract(WTON, ABI, await ethers.getSigner(MGR))
  await ton.mint(admin.address, '1000000000')
  await ton.mint(user1.address, '1000000000')
  await ton.mint(user2.address, '1000000000')
  await wton.mint(admin.address, '1000000000')
  await wton.mint(user1.address, '1000000000')
  await wton.mint(user2.address, '1000000000')
})